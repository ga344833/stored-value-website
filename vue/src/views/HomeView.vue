import axios from "axios";
export default {
  name: "HomeView",
  data() {
    return {
      user: {},
    };
  },
  created() {
    axios
      .post(
        "/api/user/info",
        {},
        {
          headers: {
            token: "666866",
          },
        }
      )
      .then((res) => {
        if (res.data.code === 0) {
          this.user = res.data.data;
        }
      });
  },
  methods: {
    click_button() {
      axios
        .post(
          "/api/user/info",
          {},
          {
            headers: {
              token: "666666",
            },
          }
        )
        .then((res) => {
          if (res.data.code === 0) {
            this.user = res.data.data;
          }
        });
    },
  },
};
</script>
